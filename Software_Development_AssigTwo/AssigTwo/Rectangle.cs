using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AssigTwo
{
    class Rectangle
    {
        /// <summary>
        /// Method to get the Area of Rectangle
        /// </summary>
        /// <returns></returns>
        public double Area(int H, int W) {

            double area = 0;

            area = H * W;

            return area;
        }
    }
}
